int ft_bouton(int i, int j, int k)
{
	if ((i > k && i < j) || (i < k && i > j))
		return (i);
	if ((j < k && j > i) || (j > k && j < i))
		return (j);
	if (i == j && j < k)
		return (j);
	else if (j == k && k < i)
		return (j);
	else
		return (k);
}
